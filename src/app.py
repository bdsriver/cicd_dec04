import math

def add(a, b):
    return a+b

def divide(a,b):
    return a/b

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b

def square(a):
    return a**2

def log(a):
    return math.log(a)

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def square_root(a):
    return math.sqrt(a)

def percentage(a,b):
    return (b/a) *100
